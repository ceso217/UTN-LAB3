int num=0;
Console.WriteLine("Estructura While:");
while (num<101)
{
    Console.WriteLine(num);
    num++;
}
num = 0;
Console.WriteLine("Estructura Do While");
do {  
    Console.WriteLine(num);
    num++;
} while (num<101);
num = 0;
Console.WriteLine("Estructura For:");
for (num = 0; num < 101; num++)
{
    Console.WriteLine(num); 
}
Console.WriteLine("Fin!");